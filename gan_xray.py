    for low, high in loader:

        low = low.to(device)
        high = high.to(device)

        # ------------------
        # Train Discriminator
        # ------------------
        fake = G(low)

        real_label = torch.full((low.size(0), 1), 0.9, device=device)
        fake_label = torch.full((low.size(0), 1), 0.1, device=device)

        # real images
        loss_real = criterion(D(high), real_label)

        # fake images
        loss_fake = criterion(D(fake.detach()), fake_label)

        loss_D = loss_real + loss_fake

        opt_D.zero_grad()
        loss_D.backward()
        opt_D.step()

        # ------------------
        # Train Generator
        # ------------------

        for _ in range(2):
            fake = G(low)  # regenerate

            loss_G_adv = criterion(D(fake), real_label)
            loss_G_l1 = l1_loss(fake, high)


