from PIL import Image
from torch.utils.data import Dataset
import torchvision.transforms as transforms
import os

class CTDataset(Dataset):
    def __init__(self, root):
        self.low_root = os.path.join(root, "Quarter Dose", "3mm", "Soft Kernel (B30)")
        self.high_root = os.path.join(root, "Full Dose", "3mm", "Soft Kernel (B30)")

        self.pairs = []

        for patient in os.listdir(self.low_root):
            low_patient = os.path.join(self.low_root, patient)
            high_patient = os.path.join(self.high_root, patient)

            if os.path.isdir(low_patient) and os.path.isdir(high_patient):

                low_imgs = sorted(os.listdir(low_patient))
                high_imgs = sorted(os.listdir(high_patient))

                # pair by index
                for l, h in zip(low_imgs, high_imgs):
                    self.pairs.append((
                        os.path.join(low_patient, l),
                        os.path.join(high_patient, h)
                    ))

        self.transform = transforms.Compose([
            transforms.Grayscale(),
            transforms.ToTensor(),
            transforms.Normalize((0.5,), (0.5,))
        ])

    def __len__(self):
        return len(self.pairs)

    def __getitem__(self, idx):
        low_path, high_path = self.pairs[idx]

        low = Image.open(low_path)
        high = Image.open(high_path)

        low = self.transform(low)
        high = self.transform(high)

        return low, high

